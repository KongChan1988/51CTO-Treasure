#求水仙花数
print('水仙花数：')
for i in range(100,1000):
    a=i//100       #百位：100地板除100=1
    b=(i%100)//10  #十位：100%100  商为1 余数（%模）为0   0地板除10=0
    c=(i%100)%10   #个位：100%100=0   0%10=0
    if i==a**3+b**3+c**3:
        print(i)

